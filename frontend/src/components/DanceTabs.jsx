const DEFAULT_STYLE = {
  icon: <MusicIcon />,
  iconBg: "bg-slate-100",
  iconColor: "text-slate-500",
  underline: "bg-slate-400",
  border: "border-slate-400",
};

export default function DanceTabs({ dances, activeId, onSelect }) {
  return (
    <div className="mb-6 grid grid-cols-2 gap-3 sm:grid-cols-3 md:grid-cols-4 lg:grid-cols-5 xl:grid-cols-6">
      {dances.map((d) => {
        const style = DANCE_STYLES[d.name] ?? DEFAULT_STYLE;
        const isActive = activeId === d.id;
        return (
          <button
            key={d.id}
            type="button"
            onClick={() => onSelect(d.id)}
            aria-pressed={isActive}
            className={[
              "group flex flex-col items-center justify-center rounded-2xl bg-white p-4 shadow-sm transition focus:outline-none focus-visible:ring-2 focus-visible:ring-brand-400 focus-visible:ring-offset-2",
              "border-2",
              isActive
                ? `${style.border} shadow-md`
                : "border-transparent hover:border-slate-200 hover:shadow-md",
            ].join(" ")}
          >
            <div
              className={`flex h-14 w-14 items-center justify-center rounded-full ${style.iconBg} ${style.iconColor} transition group-hover:scale-105`}
            >
              {style.icon}
            </div>
            <div className="mt-3 text-center text-sm font-semibold text-slate-900">
              {d.name}
            </div>
            <div
              className={`mt-2 h-0.5 w-8 rounded-full ${style.underline} ${
                isActive ? "opacity-100" : "opacity-60"
              }`}
            />
          </button>
        );
      })}
    </div>
  );
}
